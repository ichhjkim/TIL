public class basicGrammar {
	
		public static void name(String[] args) {
			char c = 'a';
			System.out.println("c ="+ c);
			int i = 10;
			System.out.println("i= "+ i);
			double d = 10.123;
			System.out.println("d= "+d);
			boolean b = true;
			System.out.println("b= "+ b);
			String s = "hello java world!!!";
			System.out.println("s = "+s);
			
		}
}
