package testpjt;

public class specialChar {
	// 특수문자
	public static void main(String[] args) {
		System.out.println("goot \t\t\t morning!");
		System.out.println("good \n morning");
		System.out.println("\' goodmorning");
		int num1 = 230;
		// 서식문자
		System.out.printf("오늘의 기온은 %d입니다\n", 10);
		System.out.printf("오늘의 기온은 %f입니다\n", 3.5);
		System.out.printf("나의 이름은 %s입니다\n", "면지");
		System.out.printf("랜덤숫자 %d입니다\n", num1);
		
	}
}
